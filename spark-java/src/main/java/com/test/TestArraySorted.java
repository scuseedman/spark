public class TestArraySorted {
	public static void main(String[] args) {
		Lianjia_sh [] arr = new Lianjia_sh[10];
		for(int i = 0; i < arr.length; i ++){
			Lianjia_sh lj = new Lianjia_sh();
			lj.setTotalPrice(new Random().nextInt(200));
			lj.setCommunityName(i + "");
			arr[i] = lj;
			
		}
		Arrays.sort(arr);
		for(int i = 0; i < arr.length ; i ++){
			System.out.println(arr[i].getCommunityName() + " ===>>> " + arr[i].getTotalPrice());
		}
	}
}
